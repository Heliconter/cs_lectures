import math
import cmath

X = 12

print('-'*80, end='\n\n')
print('math.factorial(12):', math.factorial(X))
print('math.sqrt(12):', math.sqrt(X))
print('math.pi', math.pi)
print('math.e', math.e, end='\n\n')
print('-'*80, end='\n\n')
print('cmath.sqrt(-12)', cmath.sqrt(-12), end='\n\n')
print('cmath.sqrt(-12).conjugate():', cmath.sqrt(-12).conjugate())
print('cmath.sqrt(-12).imag:', cmath.sqrt(-12).imag)  
print('cmath.sqrt(-12).real:', cmath.sqrt(-12).real, end='\n\n') 
